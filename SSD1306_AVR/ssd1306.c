void LCD_init(void)
{
	LCD_Sleep(0);
	_delay_ms(10);
	
	LCD_Command(COMAND, SSD1306_DISPLAYOFF);
	LCD_Command(COMAND, SSD1306_SETDISPLAYCLOCKDIV);
	LCD_Command(COMAND, 0x80);
	LCD_Command(COMAND, SSD1306_SETMULTIPLEX);
	if (SSD1306_HEIGHT == 32) { LCD_Command(COMAND, 0x1F); }
	else { LCD_Command(COMAND, 0x3F); }
	LCD_Command(COMAND, SSD1306_SETDISPLAYOFFSET);
	LCD_Command(COMAND, 0x00);
	LCD_Command(COMAND, SSD1306_SETSTARTLINE | 0x00);
	LCD_Command(COMAND, SSD1306_CHARGEPUMP);
	LCD_Command(COMAND, 0x14);
	LCD_Command(COMAND, SSD1306_MEMORYMODE);
	LCD_Command(COMAND, 0x00);
	LCD_Command(COMAND, SSD1306_SEGREMAP | 0x1);
	LCD_Command(COMAND, SSD1306_COMSCANDEC);
	LCD_Command(COMAND, SSD1306_SETCOMPINS);
	if (SSD1306_HEIGHT == 32) { LCD_Command(COMAND, 0x02); }
	else { LCD_Command(COMAND, 0x12); }
	LCD_Command(COMAND, SSD1306_SETCONTRAST);
	LCD_Command(COMAND, 0xCF);
	LCD_Command(COMAND, SSD1306_SETPRECHARGE);
	LCD_Command(COMAND, 0xF1);
	LCD_Command(COMAND, SSD1306_SETVCOMDETECT);
	LCD_Command(COMAND, 0x40);
	LCD_Command(COMAND, SSD1306_DISPLAYALLON_RESUME);
	LCD_Command(COMAND, SSD1306_DISPLAYON);

	LCD_Mode(0);
	// Turn display back on
	LCD_Sleep(1);

	LCD_Clear();
	LCD_Goto(0, 0);
}

void LCD_Contrast(char set_contrast)
{
	LCD_Command(COMAND, SSD1306_DISPLAYOFF);
	_delay_ms(10);
	LCD_Command(COMAND, SSD1306_SETCONTRAST);
	LCD_Command(COMAND, set_contrast);
	LCD_Command(COMAND, SSD1306_DISPLAYON);
}

void LCD_Mode(char set_mode)
{
	if(set_mode==0){ LCD_Command(COMAND, SSD1306_NORMALDISPLAY); }
	if(set_mode==1){ LCD_Command(COMAND, SSD1306_INVERTDISPLAY); }
}

void LCD_Sleep(char set)
{
	if(set==0){LCD_Command(COMAND,SSD1306_DISPLAYOFF);}
	if(set==1){LCD_Command(COMAND,SSD1306_DISPLAYON);}
}

void LCD_Command(unsigned char ControByte, unsigned char DataByte)
{
	TWI_Start();
	TWI_SendAddress(SSD1306_I2C_ADDRESS);
	TWI_SendByte(ControByte);
	TWI_SendByte(DataByte);
	TWI_Stop();
}

void LCD_Goto(unsigned char x, unsigned char y)
{
	LCD_X = x;
	LCD_Y = y;
	LCD_Command(COMAND, 0xB0 + y);
	LCD_Command(COMAND, x & 0xf);
	LCD_Command(COMAND,0x10 | (x >> 4));
}

void LCD_Goto2X(unsigned char x, unsigned char y)
{
	LCD_X = x;
	LCD_Y = y;
	LCD_Command(COMAND, 0xB0 + y);
	LCD_Command(COMAND, 2*x & 0xf);
	LCD_Command(COMAND,0x10 | (2*x >> 4));
}

void LCD_Clear(void)
{
	unsigned short i;
	unsigned short x=0;
	unsigned short y=0;
	LCD_Goto(0,0);

	for (i=0; i<(SSD1306_WIDTH*SSD1306_HEIGHT/8); i++) //(SSD1306_WIDTH*SSD1306_HEIGHT/8)
	{
		LCD_Char(' ');
		//		OLED_Data(0);
		x ++;
		if(x>SSD1306_WIDTH)
		{
			x =0;
			y++;
			LCD_Goto(0,y);
		}
	}
	LCD_X =SSD1306_DEFAULT_SPACE;
	LCD_Y =0;
}

void LCD_Char(unsigned int c)
{
	unsigned char x = 0;
	TWI_Start();
	TWI_SendAddress(SSD1306_I2C_ADDRESS);
	TWI_SendByte(DATA);
	for (x=0; x<5; x++)
	{
		TWI_SendByte(LCD_Buffer[c*5+x]);
	}
	TWI_SendByte(0x00);
	TWI_Stop();

	LCD_X += 8;
	if(LCD_X>SSD1306_WIDTH)
	{
		LCD_X = SSD1306_DEFAULT_SPACE;
	}
}

void LCD_BigNum(unsigned char num)
{
	unsigned char x = 0;
	unsigned char z = 0;

	unsigned char y_s = LCD_Y;

	if(num!=46&&num!=32&&num!=32&&num!=67){num = num - 48;}
	else
	{
		if(num==46){ num = 11; }
		if(num==32){ num = 12; }
		if(num==67){ num = 13; }
	}

	TWI_Start();
	TWI_SendAddress(SSD1306_I2C_ADDRESS);
	TWI_SendByte(DATA);
	for (x=0; x<96; x++)
	{
		TWI_SendByte(BigNum[num][x]);
		if(z>=23)
		{
			TWI_Stop();
			LCD_Goto(LCD_X,LCD_Y+1);

			z=0;
			TWI_Start();
			TWI_SendAddress(SSD1306_I2C_ADDRESS);
			TWI_SendByte(DATA);
			}else{z++;}

		}
		TWI_Stop();

		LCD_X += 23;
		LCD_Goto(LCD_X,y_s);
		if(LCD_X>SSD1306_WIDTH)
		{
			LCD_X = SSD1306_DEFAULT_SPACE;
		}
	}

	void LCD_CharBig(unsigned int c)
	{
		unsigned char x = 0;
		unsigned int m = 0;

		TWI_Start();
		TWI_SendAddress(SSD1306_I2C_ADDRESS);
		TWI_SendByte(DATA);
		for (x=0; x<5; x++)
		{
			m = LCD_Buffer[c*5+x];
			TWI_SendByte(m);
			TWI_SendByte(m);
		}
		TWI_SendByte(0x00);
		TWI_Stop();
		LCD_X += 11;
		if(LCD_X>SSD1306_WIDTH)
		{
			LCD_X = SSD1306_DEFAULT_SPACE;
		}
	}

	void LCD_Printf(unsigned char* buf, unsigned char size)
	{
		while (*buf!=0)
		{
			if(size==0)
			{
				if((LCD_X>SSD1306_WIDTH)||(LCD_X<5)){LCD_X=SSD1306_DEFAULT_SPACE;}
				LCD_Char(*buf++);
			}
			if(size==1)
			{
				if((LCD_X>SSD1306_WIDTH)||(LCD_X<5)){LCD_X=SSD1306_DEFAULT_SPACE;}
				LCD_CharBig(*buf++);
			}
			if(size==2)
			{
				if((LCD_X>SSD1306_WIDTH)||(LCD_X<24)){LCD_X=SSD1306_DEFAULT_SPACE;}
				LCD_BigNum(*buf++);
			}
		}
	}

	void LCD_DrawImage(unsigned char num_image)
	{
		unsigned int i;
		unsigned char x=0;
		unsigned char y=0;
		LCD_Goto(0,0);

		for (i=0; i<(SSD1306_WIDTH*SSD1306_HEIGHT/8); i++)
		{
			if(num_image==0)
			{
				LCD_Command(DATA, image0[i]);
			}

			x++;
			if(x>=SSD1306_WIDTH)
			{
				x=0;
				y++;
				LCD_Goto(0,y);
			}
		}
	}

	void LCD_Bat(unsigned char y,unsigned char x, unsigned char z)
	{
		unsigned char i=0;

		LCD_Goto2X(x-1,y);
		LCD_Command(DATA, 0xFF); LCD_Command(DATA, 0xFF);
		LCD_Goto2X(x+2,y);
		for (i=0; i<14; i++)
		{
			if(i<11)
			{
				if(z>=i){ LCD_Command(DATA, 0xBD); }
				else    { LCD_Command(DATA, 0x81); LCD_Command(DATA, 0x81); }
			}
			if(i==11)      {LCD_Command(DATA, 0xFF); LCD_Command(DATA, 0xFF);}
			if(i==12)      {LCD_Command(DATA, 0x3C); LCD_Command(DATA, 0x3C);}
			if(i==13)      {LCD_Command(DATA, 0x3C); LCD_Command(DATA, 0x3C);}
			LCD_Goto2X(x+i,y);
		}

	}

	void OLED_Command(int command)
	{
		TWDR=0x80;
		TWCR=(1<<TWINT)|(1<<TWEN);
		while(!(TWCR & (1<<TWINT))){};
		TWDR=command;
		TWCR=(1<<TWINT)|(1<<TWEN);
		while(!(TWCR & (1<<TWINT))){};
	}


	void OLED_Data(int data)
	{
		TWDR=data;
		TWCR=(1<<TWINT)|(1<<TWEN);
		while(!(TWCR & (1<<TWINT))){};
	}

